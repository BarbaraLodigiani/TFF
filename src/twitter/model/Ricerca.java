package twitter.model;

public class Ricerca {
	private String location;
	private String interests;
	private String radioInt;
	private String hashtags;
	private String radioHash;
	private String age;
	private String gender;
	
	
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getInterests() {
		return interests;
	}
	public void setInterests(String interests) {
		this.interests = interests;
	}
	public String getHashtags() {
		return hashtags;
	}
	public void setHashtags(String hashtags) {
		this.hashtags = hashtags;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getRadioInt() {
		return radioInt;
	}
	public void setRadioInt(String radioInt) {
		this.radioInt = radioInt;
	}
	public String getRadioHash() {
		return radioHash;
	}
	public void setRadioHash(String radioHash) {
		this.radioHash = radioHash;
	}

	
	

}
